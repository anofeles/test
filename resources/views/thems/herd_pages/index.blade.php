            <p style="text-align: justify;"><strong>The aim of eDrone proposal is to define a learning
                    environment to deliver more opportunities to access new competences related to the use
                    of drone technologies in professional activities.</strong></p>

            <p style="text-align: justify;">These new competences refer in particular to the use of advanced
                ICT solutions for the use of drones and of the data acquired through drones that may go a
                long way towards the goals and priorities in terms of optimization of the professional
                competences, that have already been stated in the <strong>Social-economic Development
                    Strategy 2020</strong> of all the <strong>Parter Countries</strong>, and in terms of
                creating new job opportunities, especially for youth that is strongly encouraged by <strong>Europe
                    2020 strategy</strong> as stated in the <strong>European Commission Communication
                    &ldquo;Youth on the Move&rdquo;</strong>.</p>

            <p style="text-align: justify;">The use of drones by professionals will open new scenarios
                requiring practical and theoretical knowledge that are beyond the simple driving and
                mantainance of drones:</p>

            <ul>

                <li style="text-align: justify;">(i) drone mechanical characteristics,</li>

                <li style="text-align: justify;">(ii) sensing equipment,</li>

                <li style="text-align: justify;">(iii) processing and use of the acquired information, and
                </li>

                <li style="text-align: justify;">(iv) national and local laws regulating their use.</li>

            </ul>

            <p style="text-align: justify;"><strong>The main objective of the eDrone project is to provide
                    higher education institutions in the Partner Countries with effective and efficient
                    instruments to setup Offices for Education for Drones (OED), for the transferring of all
                    the above mentioned knowledges to professionals of each Partner Country.</strong></p>

            <p style="text-align: justify;">Moreover, the project aims at realizing an innovative ICT-based
                infrastructure employing enhanced technologies and methodologies allowing all the Partner
                Countries to create a network for sharing educational contents and databases to
                professionals. Such infrastructure will be used to support OED in the technological and
                scientific transfer to professionals.</p>

            <p style="text-align: justify;"><strong>The Programme Countries will transfer their know-how and
                    expertise for the achievement of the eDrone objectives by training the future teachers
                    of the OED and by supporting them in the first edition of the OED.</strong></p>

